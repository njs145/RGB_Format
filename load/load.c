void RGB_fread(const char *__restrict__ __filename, FILE *fp, char **buf, __uint32_t *size)
{
    /* 파일 포인터 받아오기 */
    fp = fopen(__filename, "r");

    /* 파일 사이즈 구하기. */
    fseek(fp, 0, SEEK_END);
    *size = ftell(fp);
    fseek(fp, 0, SEEK_SET);

    /* 파일 사이즈 만큼 메모리 할당. */
    *buf = malloc(*size);

    /* 파일 포인터로부터 파일 읽기 */
    fread(*buf, sizeof(char), *size, fp);
}
